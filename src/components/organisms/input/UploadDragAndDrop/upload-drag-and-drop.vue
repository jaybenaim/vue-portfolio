<template>
  <section class="mb-5">
    <label class="label">Image</label>
    <b-field class="file">
      <b-upload
        v-model="file"
        expanded
      >
        <a class="button is-primary is-fullwidth">
          <b-icon icon="upload"></b-icon>
          <span>{{ file.name || "Click to upload"}}</span>
        </a>
      </b-upload>
    </b-field>

    <b-field>
      <b-upload
        v-model="dropFiles"
        multiple
        drag-drop
        expanded
      >
        <section class="section">
          <div class="content has-text-centered">
            <p>
              <b-icon
                icon="upload"
                size="is-medium"
              ></b-icon>
            </p>
            <p>Drop your files here or click to upload</p>
          </div>
        </section>
      </b-upload>
    </b-field>

    <div class="tags">
      <span
        v-for="(file, index) in dropFiles"
        :key="index"
        class="tag is-primary"
      >
        {{file.name}}
        <button
          class="delete is-small"
          type="button"
          @click="deleteDropFile(index)"
        ></button>
      </span>
    </div>
  </section>
</template>

<script lang="ts">
import Vue from 'vue'

export default Vue.extend({
  name: 'upload-drag-and-drop',
  props: {
    labelPosition: {
      type: String,
      default: 'inside',
      validator: (value: string) => [
        'inside',
        'on-border'
      ].indexOf(value) > -1
    }
  },
  data() {
    return {
      file: {},
      dropFiles: []
    }
  },
  methods: {
    deleteDropFile(index: number) {
      this.dropFiles.splice(index, 1)
    }
  }
})
</script>
